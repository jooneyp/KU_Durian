SINT AES_CBC_init(AES_CBC_INFO *info, SINT encrypt, SINT bits, UCHAR *user_key ,UCHAR *iv)
{	
	if( (info==NULL) || (user_key==NULL) || (iv==NULL) || (bits != 128 && bits != 192 && bits != 256) )
		return ERR_INVALID_INPUT;

	if(encrypt)
	{
		AES_encrypt_init(user_key,bits,&(info->aes_key));
	}
	else
	{
		AES_decrypt_init(user_key,bits,&(info->aes_key));
	}

	memcpy(info->ivec,iv,AES_BLOCK_SIZE);
	info->encrypt = encrypt;
	info->last_block_flag = info->buffer_length = 0;
	
	return EBD_CRYPTO_SUCCESS;
}

static void internal_aes_process_blocks(AES_KEY *aes_key, SINT encrypt, const UCHAR *ivec, const UCHAR *in, UINT inl, UCHAR *out)
{
	UINT i;
	UINT inlength = inl;
	UCHAR tmp[AES_BLOCK_SIZE];
	const UCHAR *iv = ivec;

	if(encrypt)
	{
		while (inlength >= AES_BLOCK_SIZE)
		{
			out[0] = in[0] ^ iv[0];		out[1] = in[1] ^ iv[1];
			out[2] = in[2] ^ iv[2];		out[3] = in[3] ^ iv[3];
			out[4] = in[4] ^ iv[4];		out[5] = in[5] ^ iv[5];
			out[6] = in[6] ^ iv[6];		out[7] = in[7] ^ iv[7];
			out[8] = in[8] ^ iv[8];		out[9] = in[9] ^ iv[9];
			out[10] = in[10] ^ iv[10];	out[11] = in[11] ^ iv[11];
			out[12] = in[12] ^ iv[12];	out[13] = in[13] ^ iv[13];
			out[14] = in[14] ^ iv[14];	out[15] = in[15] ^ iv[15];

			AES_encrypt_block(out,out,aes_key);

			iv = out;
			inlength -= AES_BLOCK_SIZE;
			in  += AES_BLOCK_SIZE;
			out += AES_BLOCK_SIZE;
		}

		if (inlength)
		{
			for (i = 0; i < inlength; ++i)
				out[i] = in[i] ^ iv[i];
			for (i = inlength; i < AES_BLOCK_SIZE; ++i)
				out[i] = iv[i];
			AES_encrypt_block(out,out,aes_key);
			iv = out;
		}
		memcpy((UCHAR *)ivec, iv, AES_BLOCK_SIZE);
	}
	else if (in != out) {
		while (inlength >= AES_BLOCK_SIZE)
		{
			AES_decrypt_block(in, out, aes_key);

			out[0]  ^= iv[0];	out[1]  ^= iv[1];
			out[2]  ^= iv[2];	out[3]  ^= iv[3];
			out[4]  ^= iv[4];	out[5]  ^= iv[5];
			out[6]  ^= iv[6];	out[7]  ^= iv[7];
			out[8]  ^= iv[8];	out[9]  ^= iv[9];
			out[10] ^= iv[10];	out[11] ^= iv[11];
			out[12] ^= iv[12];	out[13] ^= iv[13];
			out[14] ^= iv[14];	out[15] ^= iv[15];

			iv = in;
			inlength -= AES_BLOCK_SIZE;
			in  += AES_BLOCK_SIZE;
			out += AES_BLOCK_SIZE;
		}
		if (inlength)
		{
			AES_decrypt_block(in, out, aes_key);
			for (i = 0; i < inlength; ++i)
				out[i] = tmp[i] ^ iv[i];
			iv = in;
		}
		memcpy((UCHAR *)ivec, iv, AES_BLOCK_SIZE);
	}
	else {
		while (inlength >= AES_BLOCK_SIZE)
		{
			memcpy(tmp, in, AES_BLOCK_SIZE);
			AES_decrypt_block(in, out, aes_key);

			out[0] ^= iv[0];	out[1] ^= iv[1];
			out[2] ^= iv[2];	out[3] ^= iv[3];
			out[4] ^= iv[4];	out[5] ^= iv[5];
			out[6] ^= iv[6];	out[7] ^= iv[7];
			out[8] ^= iv[8];	out[9] ^= iv[9];
			out[10] ^= iv[10];	out[11] ^= iv[11];
			out[12] ^= iv[12];	out[13] ^= iv[13];
			out[14] ^= iv[14];	out[15] ^= iv[15];

			memcpy((UCHAR *)ivec, tmp, AES_BLOCK_SIZE);
			inlength -= AES_BLOCK_SIZE;
			in  += AES_BLOCK_SIZE;
			out += AES_BLOCK_SIZE;
		}
		if (inlength)
		{
			memcpy(tmp, in, AES_BLOCK_SIZE);

			AES_decrypt_block(in, out, aes_key);
			for (i = 0; i < inlength; ++i)
				out[i] = tmp[i] ^ iv[i];
			memcpy((UCHAR *)ivec, tmp, AES_BLOCK_SIZE);
		}
	}
}

static void internal_aes_cbc_process_enc(AES_CBC_INFO *info, UCHAR *in, SINT inLen, UCHAR *out, SINT *outLen)
{
	UINT templen = inLen&(0x0F);

	if(info->buffer_length == 0 && templen == 0)
	{		
		internal_aes_process_blocks(&(info->aes_key),info->encrypt,info->ivec,in,inLen,out);
		*outLen=inLen;
		return;
	}

	if (info->buffer_length != 0)
	{
		if (info->buffer_length + inLen < AES_BLOCK_SIZE)
		{
			memcpy(&(info->cbc_buffer[info->buffer_length]),in,inLen);
			info->buffer_length += inLen;
			*outLen=0;

			return;
		}
		else
		{
			SINT length;
			length=AES_BLOCK_SIZE-info->buffer_length;
			memcpy(&(info->cbc_buffer[info->buffer_length]),in,length);
			internal_aes_process_blocks(&(info->aes_key),info->encrypt,info->ivec,info->cbc_buffer,AES_BLOCK_SIZE,out);

			inLen -= length;
			in += length;
			out += AES_BLOCK_SIZE;
			*outLen = AES_BLOCK_SIZE;

			templen = inLen&(0x0F);
		}
	}
	else
		*outLen = 0;

	inLen -= templen;
	if (inLen > 0)
	{
		internal_aes_process_blocks(&(info->aes_key),info->encrypt,info->ivec,in,inLen,out);
		*outLen += inLen;
	}

	if (templen != 0)
		memcpy(info->cbc_buffer,&(in[inLen]),templen);

	info->buffer_length = templen;
}

static void internal_aes_cbc_process_dec(AES_CBC_INFO *info, UCHAR *in, SINT inLen, UCHAR *out, SINT *outLen)
{
	SINT updated_len;

	if(info->last_block_flag)
	{
		memcpy(out, info->cbc_last_block, AES_BLOCK_SIZE);
		out += AES_BLOCK_SIZE;
		updated_len = 1;
	}
	else
		updated_len = 0;

	internal_aes_cbc_process_enc(info,in,inLen,out,outLen);

	if (!info->buffer_length)
	{
		*outLen -= AES_BLOCK_SIZE;
		info->last_block_flag = 1;
		memcpy(info->cbc_last_block,&out[*outLen],AES_BLOCK_SIZE);
	}
	else
		info->last_block_flag = 0;

	if(updated_len)
		*outLen += AES_BLOCK_SIZE;
}

SINT AES_CBC_process(AES_CBC_INFO *info, UCHAR *in, SINT inLen, UCHAR *out, SINT *outLen)
{
	if(!info || !in || !out || !outLen || (inLen <= 0)  )
	{
		return ERR_INVALID_INPUT;
	}

	if(info->encrypt)
	{
		internal_aes_cbc_process_enc(info,in,inLen,out,outLen);
	}	
	else
	{
		internal_aes_cbc_process_dec(info,in,inLen,out,outLen);
	}

	return EBD_CRYPTO_SUCCESS;
}

SINT AES_CBC_close(AES_CBC_INFO *info, UCHAR *out, SINT *outLen)
{   
	SINT i, padlen, curloc;

	if(!info || !out || !outLen)
		return ERR_INVALID_INPUT;

	if(info->encrypt)
	{
		info->cbc_buffer[info->buffer_length] = 0x80;

		for (i=(info->buffer_length)+1; i<AES_BLOCK_SIZE; i++)
			info->cbc_buffer[i] = 0x00;

		internal_aes_process_blocks(&(info->aes_key),info->encrypt,info->ivec,info->cbc_buffer,AES_BLOCK_SIZE,out);

		*outLen = AES_BLOCK_SIZE;

		return EBD_CRYPTO_SUCCESS;
	}
	else
	{
		*outLen=0;
		
		for(curloc=15;curloc >= 0; curloc--)
		{
			if( info->cbc_last_block[curloc] == 0x80 )
			{
				if(curloc != 15)
				{
					for(i=curloc+1;i<16;i++)
					{	
						if(info->cbc_last_block[i] != 0x00)
						{
							return ERR_INVALID_PADDING;
						}
					}
					(curloc == 0) ? (padlen = 0) : (padlen = curloc); 
					break;
				}
				
				(curloc == 0) ? (padlen = 0) : (padlen = curloc); 
				break;
			}
		}
		
		if(curloc == -1)
			return ERR_INVALID_PADDING;

		for (i=0; i<padlen; ++i)
			out[i] = info->cbc_last_block[i];

		*outLen = padlen;

		return EBD_CRYPTO_SUCCESS;
	}
}

SINT AES_CBC_clear(AES_CBC_INFO *info)
{	
	if(!info)
		return ERR_INVALID_INPUT;

	memset(info, 0x00, sizeof(AES_CBC_INFO));

	return EBD_CRYPTO_SUCCESS;
}

SINT AES_CBC(SINT enc,UCHAR *user_key, UINT key_len, UCHAR *iv, UCHAR *in, UINT len, UCHAR *out)
{
	AES_CBC_INFO cbc;
	SINT interlen = 0;
	SINT padlen = 0;

	if(!AES_CBC_init(&cbc, enc, key_len*8, user_key, iv))
	{
		return EBD_CRYPTO_FAIL;
	}

	if(!AES_CBC_process(&cbc, in, len, out, &interlen))
	{
		return EBD_CRYPTO_FAIL;
	}

	if(!AES_CBC_close(&cbc, out+interlen, &padlen))
	{
		return EBD_CRYPTO_FAIL;
	}

	memset(&cbc, 0x00, sizeof(AES_CBC_INFO));

	return interlen + padlen;
}
