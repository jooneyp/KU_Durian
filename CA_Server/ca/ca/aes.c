	// round 3:
	t0 = D_ROLL(s0,s1,s2,s3,sm_rounds++); t1 = D_ROLL(s1,s2,s3,s0,sm_rounds++); t2 = D_ROLL(s2,s3,s0,s1,sm_rounds++); t3 = D_ROLL(s3,s0,s1,s2,sm_rounds++); 
	// round 4: 
	s0 = D_ROLL(t0,t1,t2,t3,sm_rounds++); s1 = D_ROLL(t1,t2,t3,t0,sm_rounds++); s2 = D_ROLL(t2,t3,t0,t1,sm_rounds++); s3 = D_ROLL(t3,t0,t1,t2,sm_rounds++); 
	// round 5: 
	t0 = D_ROLL(s0,s1,s2,s3,sm_rounds++); t1 = D_ROLL(s1,s2,s3,s0,sm_rounds++); t2 = D_ROLL(s2,s3,s0,s1,sm_rounds++); t3 = D_ROLL(s3,s0,s1,s2,sm_rounds++); 
	// round 6: 
	s0 = D_ROLL(t0,t1,t2,t3,sm_rounds++); s1 = D_ROLL(t1,t2,t3,t0,sm_rounds++); s2 = D_ROLL(t2,t3,t0,t1,sm_rounds++); s3 = D_ROLL(t3,t0,t1,t2,sm_rounds++); 
	// round 7: 
	t0 = D_ROLL(s0,s1,s2,s3,sm_rounds++); t1 = D_ROLL(s1,s2,s3,s0,sm_rounds++); t2 = D_ROLL(s2,s3,s0,s1,sm_rounds++); t3 = D_ROLL(s3,s0,s1,s2,sm_rounds++); 
	// round 8: 
	s0 = D_ROLL(t0,t1,t2,t3,sm_rounds++); s1 = D_ROLL(t1,t2,t3,t0,sm_rounds++); s2 = D_ROLL(t2,t3,t0,t1,sm_rounds++); s3 = D_ROLL(t3,t0,t1,t2,sm_rounds++); 
	// round 9: 
	t0 = D_ROLL(s0,s1,s2,s3,sm_rounds++); t1 = D_ROLL(s1,s2,s3,s0,sm_rounds++); t2 = D_ROLL(s2,s3,s0,s1,sm_rounds++); t3 = D_ROLL(s3,s0,s1,s2,sm_rounds++); 

	if (key->rounds > 10) {
		// round 10: 
		s0 = D_ROLL(t0,t1,t2,t3,sm_rounds++); s1 = D_ROLL(t1,t2,t3,t0,sm_rounds++); s2 = D_ROLL(t2,t3,t0,t1,sm_rounds++); s3 = D_ROLL(t3,t0,t1,t2,sm_rounds++); 
		// round 11: 
		t0 = D_ROLL(s0,s1,s2,s3,sm_rounds++); t1 = D_ROLL(s1,s2,s3,s0,sm_rounds++); t2 = D_ROLL(s2,s3,s0,s1,sm_rounds++); t3 = D_ROLL(s3,s0,s1,s2,sm_rounds++); 
		if (key->rounds > 12)
		{		
			// round 12: 
			s0 = D_ROLL(t0,t1,t2,t3,sm_rounds++); s1 = D_ROLL(t1,t2,t3,t0,sm_rounds++); s2 = D_ROLL(t2,t3,t0,t1,sm_rounds++); s3 = D_ROLL(t3,t0,t1,t2,sm_rounds++); 
			// round 13: 
			t0 = D_ROLL(s0,s1,s2,s3,sm_rounds++); t1 = D_ROLL(s1,s2,s3,s0,sm_rounds++); t2 = D_ROLL(s2,s3,s0,s1,sm_rounds++); t3 = D_ROLL(s3,s0,s1,s2,sm_rounds++); 
		}
	}
	rk += key->rounds << 2;

	s0 = (D_Table4[BYTE(t0, 3)] & 0xff000000) ^ (D_Table4[BYTE(t3, 2)] & 0x00ff0000) ^ (D_Table4[BYTE(t2, 1)] & 0x0000ff00) ^ (D_Table4[BYTE(t1, 0)] & 0x000000ff) ^ rk[0];
	I2OS(out     , s0);
	s1 = (D_Table4[BYTE(t1, 3)] & 0xff000000) ^ (D_Table4[BYTE(t0, 2)] & 0x00ff0000) ^ (D_Table4[BYTE(t3, 1)] & 0x0000ff00) ^ (D_Table4[BYTE(t2, 0)] & 0x000000ff) ^ rk[1];
