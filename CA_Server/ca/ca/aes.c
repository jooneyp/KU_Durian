	s2 = (D_Table4[BYTE(t2, 3)] & 0xff000000) ^ (D_Table4[BYTE(t1, 2)] & 0x00ff0000) ^ (D_Table4[BYTE(t0, 1)] & 0x0000ff00) ^ (D_Table4[BYTE(t3, 0)] & 0x000000ff) ^ rk[2];
